  @override
  void initState() {
    super.initState();
    filteredCategories = lawCategories;
  }

  //  Search function
  void _filterSearch(String query) {
    if (query.isEmpty) {
      setState(() {
        filteredCategories = lawCategories;
      });
      return;
    }

    List<LawCategory> tempList = [];

    for (var category in lawCategories) {
      List<LawChapter> tempChapters = [];
      for (var chapter in category.chapters) {
        var matchingArticles = chapter.articles
            .where((article) => article.toLowerCase().contains(query.toLowerCase()))
            .toList();
        if (matchingArticles.isNotEmpty) {
          tempChapters.add(LawChapter(title: chapter.title, articles: matchingArticles));
        }
      }
      if (tempChapters.isNotEmpty) {
        tempList.add(LawCategory(title: category.title, chapters: tempChapters));
      }
    }

    setState(() {
      filteredCategories = tempList;
    });
  }

